// ── Lookup Functions ─────────────────────────────────────────────────────────

/**
 * Get full state rules config.
 * @param {string} stateCode — 2-letter state code
 * @returns {Object|null}
 */
export function getStateRules(stateCode) {
  return MEDICAID_STATES[(stateCode || "").toUpperCase()] || null;
}

/** @returns {string[]} array of supported state codes */
export function getSupportedStates() {
  return Object.keys(MEDICAID_STATES);
}

/**
 * Check if a CDT procedure code is covered under the state's Medicaid program.
 * @param {string} stateCode
 * @param {string} cdtCode — e.g. "D2750"
 * @param {number|null} patientAge
 * @returns {{ covered: boolean|null, reason: string|null }}
 */
export function isProcedureCovered(stateCode, cdtCode, patientAge = null) {
  const rules = getStateRules(stateCode);
  if (!rules) return { covered: null, reason: "State rules not configured" };

  // Check age restrictions
  if (patientAge != null) {
    for (const ageRule of (rules.ageRestrictions || [])) {
      if (matchesCDTRange(cdtCode, ageRule.codeRange) && patientAge > ageRule.ageMax) {
        return { covered: false, reason: ageRule.notes };
      }
    }
  }

  // Check category coverage
  const category = cdtCategory(cdtCode);
  if (category && rules.coveredCategories[category] === false) {
    return { covered: false, reason: `${formatCategory(category)} not covered for adults in ${stateCode} Medicaid` };
  }

  return { covered: true, reason: null };
}

/**
 * Get frequency limit for a CDT code in a state.
 * @param {string} stateCode
 * @param {string} cdtCode
 * @returns {Object|null} — { code, description, maxPerPeriod, periodMonths, perTooth?, ageMax? }
 */
export function getFrequencyLimit(stateCode, cdtCode) {
  const rules = getStateRules(stateCode);
  if (!rules) return null;
  return (rules.frequencyLimits || []).find(f => f.code === cdtCode) || null;
}

/**
 * Check if a CDT code requires prior authorization.
 * @param {string} stateCode
 * @param {string} cdtCode
 * @returns {{ required: boolean|null, description?: string, notes?: string }}
 */
export function requiresPriorAuth(stateCode, cdtCode) {
  const rules = getStateRules(stateCode);
  if (!rules) return { required: null, reason: "State rules not configured" };

  for (const pa of (rules.priorAuthRequired || [])) {
    if (matchesCDTRange(cdtCode, pa.codeRange)) {
      return { required: true, description: pa.description, notes: pa.notes };
    }
  }
  return { required: false };
}

/**
 * Get copay amount for a CDT code.
 * @param {string} stateCode
 * @param {string} cdtCode
 * @returns {number|null} — copay in cents, or null if unknown
 */
export function getCopay(stateCode, cdtCode) {
  const rules = getStateRules(stateCode);
  if (!rules?.copays) return null;
  return rules.copays[cdtCode] ?? null;
}

/**
 * Run a comprehensive Medicaid coverage check for a set of CDT codes.
 * @param {string} stateCode
 * @param {string[]} cdtCodes
 * @param {number|null} patientAge
 * @returns {{ checks: Array<{ code, description, covered, frequency_limit, needs_pa, pa_notes, copay_cents, denial_reason }> }}
 */
export function runMedicaidCheck(stateCode, cdtCodes, patientAge = null) {
  const checks = [];

  for (const code of cdtCodes) {
    const coverage = isProcedureCovered(stateCode, code, patientAge);
    const freqLimit = getFrequencyLimit(stateCode, code);
    const pa = requiresPriorAuth(stateCode, code);
    const copay = getCopay(stateCode, code);

    checks.push({
      code,
      description: freqLimit?.description || cdtDescription(code) || code,
      covered: coverage.covered,
      denial_reason: coverage.covered === false ? coverage.reason : null,
      frequency_limit: freqLimit ? {
        max: freqLimit.maxPerPeriod,
        periodMonths: freqLimit.periodMonths,
        perTooth: freqLimit.perTooth || false,
        ageMax: freqLimit.ageMax || null,
      } : null,
      needs_pa: pa.required || false,
      pa_notes: pa.notes || null,
      pa_description: pa.description || null,
      copay_cents: copay,
    });
  }

  return { checks };
}

// ── Helpers ──────────────────────────────────────────────────────────────────

function matchesCDTRange(code, codeRange) {
  if (!code || !codeRange) return false;
  for (const rangeItem of codeRange) {
    if (rangeItem.includes("-")) {
      const [start, end] = rangeItem.split("-");
      if (code >= start && code <= end) return true;
    } else if (code === rangeItem) {
      return true;
    }
  }
  return false;
}

function cdtCategory(code) {
  if (!code) return null;
  const num = parseInt(code.replace("D", ""));
  if (isNaN(num)) return null;
  if (num >= 100 && num <= 999)   return "diagnostic";
  if (num >= 1000 && num <= 1999) return "preventive";
  if (num >= 2000 && num <= 2999) return num <= 2699 ? "basicRestorative" : "majorRestorative";
  if (num >= 3000 && num <= 3999) return "endodontic";
  if (num >= 4000 && num <= 4999) return "periodontic";
  if (num >= 5000 && num <= 5999) return "prosthodontic";
  if (num >= 6000 && num <= 6199) return "prosthodontic";
  if (num >= 7000 && num <= 7999) return "oralSurgery";
  if (num >= 8000 && num <= 8999) return "orthodontic";
  return null;
}

